
public class KevinClassBinaryBitwise {
    public static void main(String...args) throws Exception{
        int a = 11;
        int b = 16;
        //Binary
        System.out.println(a&b);
        System.out.println(a|b);
        System.out.println(a^b);

        System.out.println(7<<3);
        System.out.println(24>>2);//24 / 2^2 = 6
    }

}
